def fix_fleet_types(conn):
    """Добавляет fleet_type для самолётов"""
    cursor = conn.cursor()

    # Обновляем существующие записи
    updates = [
        ("Airbus A319-100", "narrow-body"),
        ("Airbus A320-200", "narrow-body"),
        ("Airbus A320neo", "narrow-body"),
        ("Airbus A330-900neo", "wide-body"),
        ("Embraer E190", "regional"),
    ]

    for name, fleet_type in updates:
        cursor.execute("""
            UPDATE about_us 
            SET fleet_type = ? 
            WHERE name = ? AND about_group = 'fleet'
        """, (fleet_type, name))

    # Для любых других записей флота без типа
    cursor.execute("""
        UPDATE about_us 
        SET fleet_type = 'narrow-body' 
        WHERE about_group = 'fleet' AND (fleet_type IS NULL OR fleet_type = '')
    """)

    conn.commit()
    print(f"✅ Обновлено типов самолётов: {len(updates)}")


